WITH denoms AS (
    SELECT "untrn" AS denom, "NTRN" AS token_name UNION ALL
    SELECT "ibc/DF8722298D192AAB85D86D0462E8166234A6A9A572DD4A2EA7996029DF4DB363", "WBTC" UNION ALL
    SELECT "ibc/773B4D0A3CD667B2275D5A4A7A2F0909C0BA0F4059C0B9181E680DDF4965DCC7", "TIA" UNION ALL
    SELECT "ibc/C4CFF46FD6DE35CA4CF4CE031E643C8FDC9BA4B99AE598E9B0ED98FE3A2319F9", "ATOM" UNION ALL
    SELECT "ibc/B559A80D62249C8AA07A380E2A2BEA6E5CA9A6F079C912C3A9E9B494105E4F81", "USDC" UNION ALL
    SELECT "ibc/2CB87BCE0937B1D1DFCEE79BE4501AAF3C265E923509AEAC410AD85D27F35130", "DYDX" UNION ALL
    SELECT "ibc/376222D6D9DAE23092E29740E56B758580935A6D77C24C2ABD57A6A78A1F3955", "OSMO" UNION ALL
    SELECT "ibc/A585C2D15DCD3B010849B453A2CFCB5E213208A5AB665691792684C26274304D", "WETH"
)

SELECT
  (case 
	WHEN d_in.token_name < d_out.token_name THEN CONCAT(d_in.token_name, "<>", d_out.token_name)
	ELSE CONCAT(d_out.token_name, "<>", d_in.token_name)
END)	 as pair,
amount_in,
winner
FROM swap_routes
LEFT JOIN denoms AS d_in ON token_in = d_in.denom
LEFT JOIN denoms AS d_out ON token_out = d_out.denom

